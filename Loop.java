//loops in java

public class Loop {

    public static void main(String[] args) {

        //for loop

        for (int num =1; num<=5; num++){
            System.out.println(num);
        }

        //while loop

        int num = 6 ;
        while(num<=10){
            System.out.println(num);
            num++;
        }

        // do while

        do{
            System.out.println(num++);

        }while(num<10);


    }
}
